package m.test;
import m.*;
public class Test4Funktion implements Funktion
{
	@Override
	public double get(double x) 
	{
		double mx = (-1.0) * x;
		double erg = Math.exp(mx);
		return erg;
	}
	@Override
	public String getName() 
	{
		return "Exponentialfunktion mit negativem Argument";
	}
}
